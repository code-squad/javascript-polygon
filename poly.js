//다각형의 넓이
//반지름이 10cm인 원 넓이를 출력한다.
var radius = 10;
console.log(radius * radius * Math.PI);

//밑변 10cm, 높이15cm인 사각형의 넓이를 출력한다.
var width = 10,height = 15;
console.log(width * height);

//밑변 10cm, 윗변 5cm, 높이가 12cm인 사다리꼴의 넓이를 출력한다.
var bottom = 10, top = 5;
height = 12;
console.log((bottom + top) * height / 2);